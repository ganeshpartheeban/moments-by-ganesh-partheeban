export const formatExif = (p: Photo): string => {
  const parts: string[] = [];
  if (p.focalLength) parts.push(`${p.focalLength}mm`);
  if (p.aperture) parts.push(`f/${p.aperture}`);
  if (p.iso) parts.push(`ISO ${p.iso}`);
  return parts.join(" · ");
};
